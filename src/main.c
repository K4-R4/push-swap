#include "push_swap.h"

int	main(int argc, char **argv)
{
	t_stacks	stacks;

	if (argc <= 1)
	{
		ft_printf("invalid usage\n");
		return (0);
	}
	if (!parse_arg(&stacks, argc, argv))
	{
		exit(1);
	}
	sort(&stacks);
	ft_printf("%s", stacks.instructions);
	return (0);
}
